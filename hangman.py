    word_completion = ['_'] * len(word)  # строка, содержащая символы _ на каждую букву задуманного слова
    print(f'Давайте играть в угадайку слов!{chr(int(unicode[0][1:], 16))}')
    print(f'Слово из {len(word)} букв {" ".join(word_completion)}\nДопустимое количество промахов 6☝')
    tries = 6
    loss = word
    symbol_word = []
    guessed_letters = []               # список уже названных букв
    guessed_words = []                 # список уже названных слов    
    symbol_word.extend(word)
    play(symbol_word, tries, word_completion, guessed_letters, guessed_words, loss)


