def is_valid_symbol(symbol, guessed_letters, guessed_words):
    flag = True
    for i in symbol:
        if i not in letters_ru:
            flag = False
            break
    if not flag:
        while not flag:
            symbol = input(f'Можно только буквы и слова на русском языке\U0001F928 ').upper()
            for i in symbol:
                if i in letters_ru:
                    flag = True
                else:
                    flag = False
                    break
    return symbol        


def is_valid_word(symbol, word, guessed, word_completion):
    guessed = False
    for i in symbol:
        if i in word or i in word_completion:
            guessed = True
        else:
            guessed = False
            break
    return symbol, word, guessed


